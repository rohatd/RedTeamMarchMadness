

class Team:

    def __init__(self,team_name, year, coach, players):
        self.team_name = team_name
        self.year = year
        self.coach = coach
        self.players = players
            

    def get_team_name(self):
        return self.team_name

    def get_year():
        return self.year

    def get_coach():
        return self.coach

    def get_players():
        return self.players